  <p><span class="titulo_pg2"><br />
    Raio X</span><br />
    <br />
    O <strong>Raio X </strong>&eacute; um exame que extrai imagens de qualquer parte do corpo por meio do uso de raios-x.<br />
    <br />
    &Eacute; utilizado com frequ&ecirc;ncia como m&eacute;todo inicial na avalia&ccedil;&atilde;o de v&aacute;rias patologias, pois permite a avalia&ccedil;&atilde;o anat&ocirc;mica e funcional dos sistemas e aparelhos do organismo humano.<br />
    <br />
    A Radiologia Geral ou de Raio X identifica, entre outras coisas:<br />
    - Fraturas &oacute;sseas;<br />
    - Tumores &oacute;sseos;<br />
    - Dist&uacute;rbios de crescimento e postura;<br />
    - Possibilita o estudo das articula&ccedil;&otilde;es/ artrose;<br />
    - Pneumonia;<br />
    - N&oacute;dulos pulmonares;<br />
    - Derrames pleurais;<br />
  - Sinusites;</p>
