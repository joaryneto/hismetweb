  <p><span class="titulo_pg2">Laboratorias<br />
  </span><br />
    <strong>Exame laboratorial</strong> &eacute; ao conjunto de exames e testes realizados a pedido do m&eacute;dico, realizados em laborat&oacute;rios de an&aacute;lise cl&iacute;nica, visando um diagn&oacute;stico ou confirma&ccedil;&atilde;o para uma patologia ou para um check-up (exame de rotina).</p>
  <p><span class="titulo_pg2"><a name="eletrocardiograma" id="eletrocadiograma"></a><br />
    Eletrocardiograma</span></p>
  <p><br />
  O <strong>eletrocardiograma</strong> &eacute; um teste simples que detecta e registra atividade el&eacute;trica do cora&ccedil;&atilde;o para localizar problemas card&iacute;acos. Os sinais el&eacute;tricos fazem o m&uacute;sculo card&iacute;aco contrair &agrave; medida que viajam atrav&eacute;s do cora&ccedil;&atilde;o. Quando o cora&ccedil;&atilde;o contrai ele bombeia sangue para o resto do corpo. O eletrocardiograma mostra a rapidez das batidas do cora&ccedil;&atilde;o e seu ritmo. Ele ainda registra a for&ccedil;a e ritmo dos sinais el&eacute;tricos &agrave; medida que eles passam por cada parte do cora&ccedil;&atilde;o.<br />
  <br />
  </p>
  <p>O <strong>eletrocardiograma</strong> n&atilde;o causa dor e nem causa mal algum. Um t&eacute;cnico primeiramente fixa 12 adesivos chamados eletrodos sobre a pele do cora&ccedil;&atilde;o, bra&ccedil;os e pernas. Para ajudar os eletrodos a fixarem na pele o t&eacute;cnico pode precisar raspar os pelos onde eles ser&atilde;o fixados. Depois que os eletrodos forem fixados na pele, o paciente fica deitado parado por alguns minutos enquanto os sinais el&eacute;tricos do cora&ccedil;&atilde;o s&atilde;o detectados. A m&aacute;quina ent&atilde;o registra esses sinais em um papel gr&aacute;fico ou os mostra numa tela. O teste dura em torno de 10 minutos. </p>
