  <p><span class="titulo_pg2"><br />
    Espirometria</span><br />
    <br />
    A <strong>espirometria</strong> &eacute; a medida do ar que entra e sai dos pulm&otilde;es.<br />
    <br />
    Pode ser realizada durante respira&ccedil;&atilde;o lenta ou durante 
  manobras expirat&oacute;rias for&ccedil;adas.<br />
  <br />    
  A <strong>espirometria</strong> &eacute; um teste que auxilia na preven&ccedil;&atilde;o e 
    permite o diagn&oacute;stico e a quantifica&ccedil;&atilde;o dos dist&uacute;rbios 
    ventilat&oacute;rios. A espirometria deve ser parte integrante da 
    avalia&ccedil;&atilde;o de funcion&aacute;rios expostos a poeira.<br />
    <br />
    A <strong>espirometria</strong> &eacute; um exame peculiar em medicina, posto 
    que exige a compreens&atilde;o e colabora&ccedil;&atilde;o do paciente,<br />
    equipamentos exatos e emprego de t&eacute;cnicas padronizadas 
  aplicadas por pessoal especialmente treinado.</p>
