  <p><span class="titulo_pg2">E</span><span class="titulo_pg2">letroencefalograma</span><br />
    <br />
  A <strong>Eletroencefalografia (EEG) </strong>&eacute; o estudo do registro gr&aacute;fico das correntes el&eacute;tricas desenvolvidas no enc&eacute;falo, realizado atrav&eacute;s de eletrodos aplicados no couro cabeludo, na superf&iacute;cie encef&aacute;lica, ou at&eacute; mesmo dentro da subst&acirc;ncia encef&aacute;lica.<br />
  <br />
  Colocando-se el&eacute;ctrodos em posi&ccedil;&otilde;es pr&eacute;-definidas sobre o couro cabeludo do paciente, um amplificador aumenta a intensidade dos potenciais el&eacute;ctricos que posteriormente ser&atilde;o plotados num gr&aacute;fico digital. As altera&ccedil;&otilde;es dos padr&otilde;es da normalidade permitem ao m&eacute;dico fazer a correla&ccedil;&atilde;o cl&iacute;nica com os achados do EEG. Podemos observar descargas de ondas anormais em forma de pontas por exemplo (picos de onda), complexos ponta-onda ou actividades lentas focais ou generalizadas. <br />
  <br />
  As indica&ccedil;&otilde;es destes exames s&atilde;o: avalia&ccedil;&atilde;o em exame admissional, s&iacute;ndromes epil&eacute;pticas, avalia&ccedil;&atilde;o de coma, morte encef&aacute;lica, intoxica&ccedil;&otilde;es, encefalites, s&iacute;ndromes demenciais, crises n&atilde;o epil&eacute;pticas e dist&uacute;rbios metab&oacute;licos.</p>
