<h2>Exames</h2>
<br>
  <p><span class="titulo_pg2"><br />
    Audiometria</span><br />
    <br />
    A <strong>audiometria</strong> &eacute; um exame que avalia a audi&ccedil;&atilde;o das pessoas. Quando detecta qualquer anormalidade auditiva permite medir o seu grau e tipo de altera&ccedil;&atilde;o, assim como orienta as medidas preventivas ou curativas a serem tomadas, evitando assim o agravamento. <br />
    <br />
    Os principais tipos de <strong>audiometria</strong> s&atilde;o a audiometria tonal, que &eacute; considerado um teste subjetivo para avaliar o grau e o tipo de perda auditiva e a audiometria vocal, que pesquisa a capacidade de compreens&atilde;o da fala humana. <br />
    <br />
  Estes exames s&atilde;o feitos com o paciente dentro de uma cabine ac&uacute;stica, visando isol&aacute;-lo do ru&iacute;do ambiental e utiliza o equipamento chamado audi&ocirc;metro. <br />
  </p>
