<h2>EXAME M&Eacute;DICO ADMISSIONAL</h2>
<br>

O exame m&eacute;dico &eacute; obrigat&oacute;rio por ocasi&atilde;o da admiss&atilde;o e tem por finalidade constatar a capacidade f&iacute;sica e mental do empregado para o exerc&iacute;cio da fun&ccedil;&atilde;o a que est&aacute; sendo contratado. Tal exame deve ser efetuado antes que o trabalhador assuma suas atividades, e, faz parte das exig&ecirc;ncias do Programa do Controle M&eacute;dico de Sa&uacute;de Ocupacional criado pela Portaria 24/94 do Secret&aacute;rio de Seguran&ccedil;a e Sa&uacute;de do Trabalho, e, deve ser implantado obrigatoriamente por todos os empregadores. Observa-se que em hip&oacute;tese alguma pode ser feito teste de gravidez na admiss&atilde;o de empregadas, conforme disp&otilde;e a Lei 9.029/95. 

  <div class="titulo_pg2">EXAME M&Eacute;DICO PERI&Oacute;DICO</div>

Periodicamente os exames m&eacute;dicos feitos por ocasi&atilde;o da admiss&atilde;o devem ser renovados. Esta periodicidade varia de acordo com a atividade e risco que o empregado est&aacute; exposto. Podendo variar de 6 meses &agrave; 2 anos. 
  
  <div class="titulo_pg2">EXAME M&Eacute;DICO DE RETORNO AO TRABALHO</div>

  O exame m&eacute;dico de retorno ser&aacute; exigido sempre que o empregado se ausentar do trabalho por per&iacute;odo igual ou superior a 30 dias por motivo de doen&ccedil;a, acidente ou parto, e, dever&aacute; ser realizado no dia do seu retorno ao trabalho.<br />

  <div class="titulo_pg2">EXAME M&Eacute;DICO DE MUDAN&Ccedil;A DE FUN&Ccedil;&Atilde;O</div> 
  
  Antes de mudar a fun&ccedil;&atilde;o do trabalhador, a empresa est&aacute; obrigada a submet&ecirc;-lo a um exame m&eacute;dico, observando que, por mudan&ccedil;a de fun&ccedil;&atilde;o entende-se toda e qualquer altera&ccedil;&atilde;o de atividade, posto de trabalho ou de setor que implique na exposi&ccedil;&atilde;o do trabalhador a risco diferente daquele que estava exposto antes da mudan&ccedil;a.
  
  <div class="titulo_pg2">EXAME M&Eacute;DICO DEMISSIONAL</div>

  Deve ser realizado dentro dos 15 dias que antecedem a sa&iacute;da do empregado, quando o aviso-pr&eacute;vio for trabalhado ou quando se tratar de extin&ccedil;&atilde;o do contrato a prazo ou at&eacute; a data da homologa&ccedil;&atilde;o quando o aviso-pr&eacute;vio for indenizado ou na inexist&ecirc;ncia do aviso.
  
