<h2>PPR - Programa de Prote&ccedil;&atilde;o Respirat&oacute;ria (PPR), conforme Instru&ccedil;&atilde;o Normativa da Portaria 3214/78 do MTE.</h2>
<br>
 
<p>O programa de prote&ccedil;&atilde;o respirat&oacute;ria serve para que o empres&aacute;rio tenha certeza de que o seu funcion&aacute;rio est&aacute; saud&aacute;vel hoje e que continuar&aacute; no futuro tamb&eacute;m. </p>
<br />

<p>&Eacute; obrigat&oacute;rio para as empresas em que temos trabalhadores em ambientes com material em suspens&atilde;o (aerodispers&oacute;ides) e considerados prejudiciais &agrave; sa&uacute;de.<br />
</p>
<p class="titulo_pg2">Objetivos</p>
<p>Manter o controle para o correto uso de protetores das vias a&eacute;reas (respirat&oacute;rias), e dos funcion&aacute;rios envolvidos em ambientes contendo elementos em suspens&atilde;o (aerodispers&oacute;ides, n&eacute;voas, fumos, radionucl&iacute;deos, neblina, fuma&ccedil;a, vapores, gases) que provoquem danos &agrave;s vias a&eacute;reas (pulm&atilde;o, traqu&eacute;ia, fossas nasais, faringe). </p>
<br />
<p>Utilizam-se protetores quando ocorrem emerg&ecirc;ncias, quando medidas de controle coletivo n&atilde;o s&atilde;o vi&aacute;veis, ou enquanto n&atilde;o est&atilde;o sendo implantadas ou est&atilde;o em fase de implanta&ccedil;&atilde;o. </p>
<p class="titulo_pg2">Responsabilidades<br />
</p>
<p>O administrador da empresa &eacute; o principal respons&aacute;vel por tudo que ocorrer dentro da mesma, seja por culpa (contratual, extracontratual ou aquiliana, &quot;in eligendo&quot;, &quot;in vigilando&quot;, &quot;in committendo&quot;, &quot;in omittendo&quot;, &quot;in custodiendo&quot;, &quot;in concreto&quot; ou &quot;in abstracto&quot;) , dolo, imprud&ecirc;ncia ou neglig&ecirc;ncia. </p>
<br />
<p>&Eacute; o administrador que poder&aacute; realizar altera&ccedil;&otilde;es no programa de prote&ccedil;&atilde;o respirat&oacute;ria. </p><br />

<p>O Engenheiro do Trabalho, M&eacute;dico Ocupacional ou T&eacute;cnico de Seguran&ccedil;a do Trabalho se constituem nos respons&aacute;veis pelo acompanhamento das atividades e sua implanta&ccedil;&atilde;o efetiva.</p><br />

<p>De acordo com a Portaria n&uacute;mero 1 de 11 de Abril de 1994, emitida pelo Minist&eacute;rio do Trabalho, cujo conte&uacute;do estabelece um regulamento t&eacute;cnico sobre uso de equipamentos de prote&ccedil;&atilde;o respirat&oacute;ria, todo empregador dever&aacute; adotar um conjunto de medidas com a finalidade de adequar a utiliza&ccedil;&atilde;o de equipamentos de prote&ccedil;&atilde;o respirat&oacute;ria - EPR, quando necess&aacute;rio para complementar as medidas de prote&ccedil;&atilde;o eletivas implementadas, ou com a finalidade de garantir uma completa prote&ccedil;&atilde;o ao trabalhador contra os riscos existentes nos ambientes de trabalho.</p>
