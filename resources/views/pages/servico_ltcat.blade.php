<h2>LTCAT- Laudo Técnico das Condições do Ambiente de Trabalho</h2>
<br>

O Laudo Técnico das Condições do Ambiente de Trabalho – LTCAT, exigível desde 1960 para o ruído e estendido pela Lei nº 9.032 de 1995 para os demais agentes ambientais, é uma peça técnica, assinada por um especialista (médico do trabalho ou engenheiro de segurança) que deve, entre outros, apresentar conclusão clara e objetiva acerca da efetiva exposição do trabalhador a agentes ambientais para efeitos de concessão da aposentadoria especial.<br />
<br />
Em regra, o LTCAT é individual. Mas o INSS tem aceitado LTCAT coletivo, desde que se consiga enquadrar o trabalhador através de suas informações. Por exemplo: no ano de 1998, para o cargo de caldeireiro do setor da caldeiraria, o LTCAT atestava que havia exposição ao calor acima dos limites de tolerância. Um determinado trabalhador comprova que exercia aquele cargo e era lotado naquele setor, portanto tinha direito à aposentadoria especial.<br />
<br />
Este documento deve ser compatível com a documentação ambiental, em especial ao PPRA e suas avaliações anuais.

